            <h2 style="font-size: 20px; font-weight: 700; color: #1e293b; margin-bottom: 15px; border-left: 4px solid #10b981; padding-left: 15px;">2. Données Collectées</h2>
            <p>Nous collectons uniquement les informations strictement nécessaires à la validation de la présence au travail et à la gestion administrative :</p>
            <ul style="margin-bottom: 20px;">
                <li><strong>Données des Administrateurs / Managers :</strong> Nom, prénom, adresse e-mail professionnelle, numéro de téléphone, nom de l'entreprise et informations de facturation.</li>
                <li><strong>Données des Employés :</strong> Nom, prénom, matricule interne, site d'affectation et photographies (le cas échéant).</li>
                <li><strong>Données de Pointage (Données Techniques) :</strong>
                    <ul style="margin-top:10px;">
                        <li><strong>Horodatage :</strong> Date et heure exactes de chaque scan.</li>
                        <li><strong>Géolocalisation (GPS) :</strong> Coordonnées de latitude et longitude relevées exclusivement lors de l'action de pointage.</li>
                        <li><strong>Identifiant de l'appareil (Device ID) :</strong> Empreinte unique du smartphone utilisé pour garantir l'unicité du compte et prévenir l'utilisation frauduleuse d'un terminal pour plusieurs employés.</li>
                    </ul>
                </li>
                <li><strong>Données Financières :</strong> Les transactions d'abonnement effectuées via Mobile Money sont traitées par notre partenaire sécurisé CinetPay. RHXtimes ne stocke aucune coordonnée bancaire ou code secret.</li>
            </ul>
        </section>

        <section style="margin-bottom: 40px;">
            <h2 style="font-size: 20px; font-weight: 700; color: #1e293b; margin-bottom: 15px; border-left: 4px solid #10b981; padding-left: 15px;">3. Finalités du Traitement</h2>
            <p>Les données sont traitées pour les objectifs suivants :</p>
            <ol>
                <li><strong>Validation de la présence physique :</strong> Vérifier que l'employé est réellement présent sur le site de travail (Geofencing).</li>
                <li><strong>Lutte contre la fraude :</strong> Empêcher le pointage à distance ou pour le compte d'un tiers.</li>
                <li><strong>Gestion de la paie :</strong> Calculer automatiquement les heures de travail, les retards et générer les rapports de paie conformes au système comptable OHADA.</li>
                <li><strong>Sécurisation du service :</strong> Garantir l'intégrité des données via une architecture multi-tenant isolant chaque entreprise cliente.</li>
            </ol>
        </section>

        <section style="margin-bottom: 40px; background: #f0fdf4; padding: 25px; border-radius: 15px; border: 1px solid #bbf7d0;">
            <h2 style="font-size: 20px; font-weight: 700; color: #166534; margin-bottom: 15px;">4. Spécificité de la Géolocalisation</h2>
            <p><strong>RHXtimes n'est pas un système de surveillance continue (tracking).</strong></p>
            <ul>
                <li>La géolocalisation est activée uniquement au moment précis où l'employé clique sur "Pointer".</li>
                <li>Dès que le scan est validé, le service GPS est interrompu. Aucun relevé de position n'est effectué en dehors des heures et lieux de pointage déclarés.</li>
            </ul>
        </section>

        <section style="margin-bottom: 40px;">
            <h2 style="font-size: 20px; font-weight: 700; color: #1e293b; margin-bottom: 15px; border-left: 4px solid #10b981; padding-left: 15px;">5. Sécurité et Conservation</h2>
            <ul style="margin-bottom: 20px;">
                <li><strong>Chiffrement :</strong> Toutes les données transitent via des protocoles sécurisés SSL/TLS.</li>
                <li><strong>Stockage Hors-ligne :</strong> En cas de perte de connexion, les données sont stockées de manière chiffrée dans la mémoire locale sécurisée de l'application (PWA) et synchronisées dès le rétablissement du réseau.</li>
                <li><strong>Sauvegardes :</strong> Des sauvegardes automatiques quotidiennes assurent la pérennité des historiques.</li>
                <li><strong>Durée de conservation :</strong> Les données sont conservées pendant toute la durée de l'abonnement. En cas de résiliation, l'employeur peut exporter son historique intégral avant la suppression définitive des données de nos serveurs.</li>
            </ul>
        </section>

